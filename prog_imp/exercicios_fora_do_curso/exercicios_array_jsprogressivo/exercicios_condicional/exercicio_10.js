// Faça um script que leia um número e exiba o dia correspondente da semana. 
//(1-Domingo, 2- Segunda, etc.), se digitar outro valor deve aparecer valor inválido.

let numero = 2;
if (numero == 1)
{
    console.log(numero + '-Domingo')
}
else if (numero == 2)
{
    console.log(numero + '-Segunda')
}
else if (numero == 3)
{
    console.log(numero + '-Terça')
}
else if (numero == 4)
{
    console.log(numero + '-Quarta')
}
else if (numero == 5)
{
    console.log(numero + '-Quinta')
}
else if (numero == 6)
{
    console.log(numero + '-Sexta')
}
else if (numero == 7)
{
    console.log(numero + '-Sábado')
}